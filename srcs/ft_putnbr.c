#include "libft.h"

void	ft_putnbr(int n)
{
	int i;

	if (n == -2147483648)
	{
		ft_putstr("-2147483648");
		return ;
	}
	if (n < 0)
	{
		n = -n;
		ft_putchar('-');
	}
	i = n;
	if (n >= 0 && n <= 9)
		ft_putchar(i + '0');
	if (n > 9)
	{
		ft_putnbr(i / 10);
		ft_putchar((i % 10) + '0');
	}
}
