#include "libft.h"

char	*ft_strtrim(const char *s)
{
	int	start;
	int	end;

	if (!s)
		return (NULL);
	start = 0;
	end = ft_strlen(s) - 1;
	while ((s[start] == ' ' || s[start] == '\t' || s[start] == '\n') &&
			s[start])
		start++;
	while ((s[end] == ' ' || s[end] == '\t' || s[end] == '\n') && s[end] &&
			end > start)
		end--;
	return (ft_strsub(s, start, (end - start + 1)));
}
